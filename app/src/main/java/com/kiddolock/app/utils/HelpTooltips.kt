    /**
     * הצגת דיאלוג הסבר על נושא מסוים
     */
    fun show(context: Context, topic: HelpTopic) {
        AlertDialog.Builder(context)
            .setTitle("ℹ️ " + topic.title)
            .setMessage(topic.body)
            .setPositiveButton("הבנתי", null)
            .setCancelable(true)
            .show()
    }

    /**
     * חיבור View (אייקון ?) לפתיחת דיאלוג בלחיצה
     */
    fun attach(view: View, topic: HelpTopic) {
        view.setOnClickListener { show(view.context, topic) }
        view.contentDescription = "עזרה: " + topic.title
    }

    /**
     * חיבור מספר אייקוני עזרה בו-זמנית
     */
    fun attachAll(activity: Activity, mapping: Map<Int, HelpTopic>) {
        for ((viewId, topic) in mapping) {
            val v = activity.findViewById<View>(viewId)
            if (v != null) attach(v, topic)
        }
    }
